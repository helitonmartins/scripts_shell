${USERADD} -s /bin/bash -d ${BACULA_WD}  bacula
${CHOWN} -R bacula:bacula ${BACULA_WD} ${BACULA_PID} ${BACULA_LOG} ${BACULA_BASE}

### GETTING THE BACULA SOURCES
